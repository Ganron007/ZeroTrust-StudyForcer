const STORAGE_KEY = "ztsf:personality-mode"

export function getSavedMode(): PersonalityMode {
  try {
    const raw = localStorage.getItem(STORAGE_KEY)
    if (raw && ["standard", "drill-sergeant", "cyberpunk", "script-kiddie", "zero-trust-audit", "influencer", "politician", "linkedin-lunatic", "true-crime", "weather-anchor", "passive-aggressive", "conspiracy", "elderly"].includes(raw)) {
      return raw as PersonalityMode
    }
  } catch { /* ignore */ }
  return "standard"
}

export function saveMode(mode: PersonalityMode): void {
  try {
    localStorage.setItem(STORAGE_KEY, mode)
  } catch { /* ignore */ }
}

/** Get a label string for the given mode and key. Falls back to standard mode, then `key` if not found. */
export function getLabel(mode: PersonalityMode, key: string): string {
  return LABELS[mode]?.[key] ?? LABELS["standard"]?.[key] ?? key
}

/** Get a toast template for the given mode and key. Falls back to standard mode, then key. */
export function getToast(mode: PersonalityMode, key: string): string {
  return TOASTS[mode]?.[key] ?? TOASTS["standard"]?.[key] ?? key
}

/** Get an empty-state message for the given mode and key. Falls back to standard, then key. */
export function getEmpty(mode: PersonalityMode, key: string): string {
  return EMPTY[mode]?.[key] ?? EMPTY["standard"]?.[key] ?? key
}

/** Get a greeting for the given mode and time-of-day key. Falls back to standard, then key. */
export function getGreeting(mode: PersonalityMode, timeKey: "morning" | "afternoon" | "evening"): string {
  return GREETINGS[mode]?.[timeKey] ?? GREETINGS["standard"]?.[timeKey] ?? "Good {time}"
}

/** Get a loading message for the given mode and key. Falls back to standard, then key. */
export function getLoading(mode: PersonalityMode, key: string): string {
  return LOADING[mode]?.[key] ?? LOADING["standard"]?.[key] ?? "Loading..."
}

/** Get the tips array for the given mode. Falls back to standard. */
export function getTips(mode: PersonalityMode): string[] {
  return TIPS[mode] ?? TIPS["standard"] ?? []
}

/** Format a template string by replacing `{key}` placeholders with values from `params`. */
export function formatStr(template: string, params: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`))
}
